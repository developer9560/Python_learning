#set is mutable but their element are immutable
# add(el) it will add an element in the set
# remove(el) it will remove an element from the set
# clear() it will remove all the elements from the set
# pop() it will remove the last element from the set
set = {1,2,3,4,5,6}
print(set)
set.add(7)
print(set)
set.remove(5)
print(set)
set.pop()
print(set)
set.clear()
print(set)
